package wispmanager.page;

import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import wispmanager.entities.SerieTicket;
import wispmanager.entities.jpa.SerieTicketJpaController;

/**
 *
 * @author mbohm
 */
public class SerieTicketConverter implements Converter {

    public Object getAsObject(FacesContext facesContext, UIComponent component, String string) {
        if (string == null || string.length() == 0) {
            return null;
        }
        Long id = new Long(string);
        SerieTicketJpaController controller = (SerieTicketJpaController) facesContext.getApplication().getELResolver().getValue(facesContext.getELContext(), null, "SerieTicketJpaController");
        Object obj=controller.findSerieTicket(id);
        return  obj;
    }

    public String getAsString(FacesContext facesContext, UIComponent component, Object object) {
        if (object == null) {
            return null;
        }
        if (object instanceof SerieTicket) {
            SerieTicket o = (SerieTicket) object;
            Long id=o.getId();
            return o.getId() == null ? "" : o.getId().toString();
        } else {
            throw new IllegalArgumentException("object " + object + " is of type " + object.getClass().getName() + "; expected type: wispmanager.entities.SerieTicketConverter");
        }
    }

}
